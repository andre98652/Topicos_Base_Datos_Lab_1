def mostrar_menu():
    while True:
        print("\n" + "="*45)
        print("🎓 SISTEMA DE RECOMENDACIÓN ACADÉMICA EPIS")
        print("="*45)
        print("1. Recomendar cursos por interés y semestre")
        print("2. Buscar cursos similares a uno (Relación)")
        print("3. Buscar cursos por perfil de habilidades")
        print("4. Salir")

        opcion = input("\nSeleccione una opción (1-4): ")

        if opcion == '1':
            interes = input("¿Qué te gustaría aprender? (ej: programación, redes, seguridad): ")
            sem = int(input("¿En qué semestre estás? (1-10): "))
            r = recomendar_cursos(interes, sem)
            print("\n✨ CURSOS RECOMENDADOS PARA TI:")
            for i in range(len(r['ids'][0])):
                meta = r['metadatas'][0][i]
                print(f" • {meta['nombre']} | Semestre: {meta['semestre']} | Área: {meta['area']}")

        elif opcion == '2':
            nombre = input("Ingresa el nombre del curso base (ej: Inteligencia Artificial): ")
            r = cursos_similares(nombre)
            if r:
                print(f"\n📚 CURSOS RELACIONADOS TEMÁTICAMENTE:")
                # Empezamos en 1 para no mostrar el mismo curso que ingresamos
                for i in range(1, len(r['ids'][0])):
                    meta = r['metadatas'][0][i]
                    print(f" • {meta['nombre']} | Semestre: {meta['semestre']}")

        elif opcion == '3':
            habs = input("Ingresa tus habilidades (separadas por comas): ").split(",")
            ar = input("Área de interés (básica / especialidad / electivo): ").lower()
            r = buscar_por_perfil(habs, ar)
            print("\n🎯 CURSOS QUE COINCIDEN CON TU PERFIL:")
            for i in range(len(r['ids'][0])):
                meta = r['metadatas'][0][i]
                print(f" • {meta['nombre']} | Requisito: {meta['prereq']}")

        elif opcion == '4':
            print("\n¡Gracias por usar el sistema EPIS! Cerrando sesión...")
            break
        else:
            print("Opcíon inválida. Intente de nuevo.")

