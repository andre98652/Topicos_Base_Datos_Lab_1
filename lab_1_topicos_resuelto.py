"""##Implementación de la función de búsqueda avanzada"""

def buscar_articulos(consulta, area=None, anio_desde=None, anio_hasta=None, max_turnitin=20, top_k=5):
    filtros = []

    # Agregar filtro de área
    if area:
        filtros.append({"area": {"$eq": area}})

    # Agregar filtro de Turnitin
    filtros.append({"turnitin": {"$lte": max_turnitin}})

    # Agregar filtros de rango de año
    if anio_desde:
        filtros.append({"anio": {"$gte": anio_desde}})
    if anio_hasta:
        filtros.append({"anio": {"$lte": anio_hasta}})

    # Combinar todos los filtros con $and
    filtro_final = {"$and": filtros} if len(filtros) > 1 else filtros[0]

    res = repositorio_epis.query(
        query_texts=[consulta],
        n_results=top_k,
        where=filtro_final
    )

    # Formateo de salida
    print(f"\n🔍 Buscando: '{consulta}' | Filtros: Area={area}, Turnitin Max={max_turnitin}%")
    for i in range(len(res['ids'][0])):
        print(f"[{i+1}] ID: {res['ids'][0][i]} | Similitud: {1 - res['distances'][0][i]:.4f}")
        print(f"    Autor: {res['metadatas'][0][i]['autor']} | Area: {res['metadatas'][0][i]['area']} | Año: {res['metadatas'][0][i]['anio']}")
        print(f"    Texto: {res['documents'][0][i][:80]}...")

