def recomendar_cursos(intereses, semestre_max, top_k=3):
    """Recomienda cursos basados en intereses filtrando por semestre."""
    print(f"\nBuscando recomendaciones para: '{intereses}' (Hasta semestre {semestre_max})...")
    res = coleccion_cursos.query(
        query_texts=[intereses],
        n_results=top_k,
        where={"semestre": {"$lte": semestre_max}}
    )
    return res

def cursos_similares(nombre_curso, n=3):
    """Encuentra cursos parecidos a uno existente en la base de datos."""
    # 1. Recuperamos el curso base para obtener su descripción
    datos_base = coleccion_cursos.get(where={"nombre": nombre_curso})

    if not datos_base['documents']:
        print(f"❌ Error: El curso '{nombre_curso}' no existe. Verifica mayúsculas y tildes.")
        return None

    # 2. Buscamos similares usando la descripción del curso base
    print(f"\nBuscando cursos similares a '{nombre_curso}'...")
    res = coleccion_cursos.query(
        query_texts=[datos_base['documents'][0]],
        n_results=n + 1 # +1 porque se encontrará a sí mismo
    )
    return res

def buscar_por_perfil(habilidades, area_filtro='especialidad'):
    """Busca cursos según un perfil de habilidades y área específica."""
    perfil_texto = ", ".join(habilidades)
    print(f"\nBuscando cursos de {area_filtro} para el perfil: {perfil_texto}...")
    res = coleccion_cursos.query(
        query_texts=[perfil_texto],
        n_results=3,
        where={"area": {"$eq": area_filtro}}
    )
    return res

